function getStockStatus(
	onHand: number,
	reserved: number,
	reorderPoint: number,
): StockStatus {
	const available = onHand - reserved;
	if (available <= 0) return "out-of-stock";
	if (available <= reorderPoint) return "low-stock";
	return "in-stock";
}

export function buildSearchableVariants(): SearchableVariant[] {
	return variants.map((v) => {
		const items = inventoryItems.filter((ii) => ii.variantId === v.id);
		const inventoryItemsMapped = items.map((ii) => ({
			warehouseId: ii.warehouseId,
			warehouseName: ii.warehouse.name,
			warehouseCode: ii.warehouse.code,
			quantityOnHand: ii.quantityOnHand,
			quantityReserved: ii.quantityReserved,
			quantityAvailable: ii.quantityOnHand - ii.quantityReserved,
			reorderPoint: ii.reorderPoint,
			status: getStockStatus(
				ii.quantityOnHand,
				ii.quantityReserved,
				ii.reorderPoint,
			),
		}));

		const totalOnHand = items.reduce((s, ii) => s + ii.quantityOnHand, 0);
		const totalAvailable = items.reduce(
			(s, ii) => s + (ii.quantityOnHand - ii.quantityReserved),
			0,
		);

		let overallStatus: StockStatus = "in-stock";
		if (totalAvailable <= 0) overallStatus = "out-of-stock";
		else if (inventoryItemsMapped.some((i) => i.status !== "in-stock"))
			overallStatus = "low-stock";

		return {
			...v,
			inventoryItems: inventoryItemsMapped,
			totalOnHand,
			totalAvailable,
			overallStatus,
		};
	});
}

export function getOverviewStats() {
	const searchable = buildSearchableVariants();
	const totalSkus = variants.length;
	const totalOnHand = inventoryItems.reduce(
		(s, ii) => s + ii.quantityOnHand,
		0,
	);
	const openAlerts = alerts.filter((a) => a.alertStatus === "OPEN").length;
	const outOfStock = searchable.filter(
		(v) => v.overallStatus === "out-of-stock",
	).length;
	const lowStock = searchable.filter(
		(v) => v.overallStatus === "low-stock",
	).length;

	return { totalSkus, totalOnHand, openAlerts, outOfStock, lowStock };
}
